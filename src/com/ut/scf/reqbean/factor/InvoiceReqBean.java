package com.ut.scf.reqbean.factor;

import java.util.List;

public class InvoiceReqBean {
	/**
	 * 企业ID
	 */
	private String corpId;

	/**
	 * 合同号
	 */
	private String cntrctNo;

	/**
	 * 协议编号
	 */
	private String cntNo;

	/**
	 * 发票状态 已登记 已确认 已确认已转让 已融资 已全额付款
	 */
	private String invStatus;

	/**
	 * 发票状态 多种
	 */
	private List<String> invStatusList;

    /**
     * 结算类型
     */
	private String settlementTp;

	public String getCorpId() {
		return corpId;
	}

	public void setCorpId(String corpId) {
		this.corpId = corpId;
	}

	public String getCntrctNo() {
		return cntrctNo;
	}

	public void setCntrctNo(String cntrctNo) {
		this.cntrctNo = cntrctNo;
	}

	public String getInvStatus() {
		return invStatus;
	}

	public void setInvStatus(String invStatus) {
		this.invStatus = invStatus;
	}

	public List<String> getInvStatusList() {
		return invStatusList;
	}

	public void setInvStatusList(List<String> invStatusList) {
		this.invStatusList = invStatusList;
	}

	public String getCntNo() {
		return cntNo;
	}

	public void setCntNo(String cntNo) {
		this.cntNo = cntNo;
	}

    public String getSettlementTp() {
        return settlementTp;
    }

    public void setSettlementTp(String settlementTp) {
        this.settlementTp = settlementTp;
    }
}